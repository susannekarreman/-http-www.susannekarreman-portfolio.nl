<p style="text-align:justify;">
De define fase ben ik begonnen met vanuit mijn lab georganiseerde co-creatie met de huidige NRC lezer. Ik heb hiervoor zelf een creatieve sessie ontworpen. Deze sessie heeft geleid tot veel nieuwe inzichten zoals:
<li>De NRC-lezer wil graag betrokken worden bij de krant.</li>
<li>De abonnee voelt zich sterk verbonden met het merk NRC en weet waar hij of zij voor betaald.</li>
<li>Op zaterdag wordt de krant nog op papier gelezen.</li>
</p>
<p style="text-align:justify;">
De testresultaten laten zien, dat de NRC-lezer een relatie met het merk aan is gegaan, doordat ze zich verbonden voelen met het merk NRC. Dit is ontstaan doordat ze de NRC geloven en voor kwaliteitsjournalistiek willen betalen omdat ze weten wat het inhoudt en er waarden aan hechten. </p>
<p style="text-align:justify;">
Hierna heb ik samen met mijn team de inside cards opgesteld. d
Deze zijn later vormgegeven door een teamgenoot. Tijdens het opstellen hiervan viel het op dat de insights eigenlijk goed op elkaar aansloten, we wilden kijken naar de toekomst en dat de band tussen lezer en krant persoonlijker wordt. Hiernaast kwam ook de noodzaak van betrouwbaarheid heel erg naar voren, van zowel uit de NRC als de doelgroep. 
</p>
<p style="text-align:justify;">
Tijdens de define fase kwamen wij een beetje vast te zitten, omdat we zo veel informatie hadden maar eigenlijk nog geen duidelijke richtlijnen hadden zoals een goed hwk-vraag en design principles. We kregen toen de kans om tijdens de studio aan ons design principles en richtlijnen te werken. Dit hebben we gedaan door eerst een creatieve sessie te houden. We hebben vervolgens heleboel ‘hoe kunnen we’ vragen gemaakt op basis van de debrief en onderzoek die we gedaan hadden. Vervolgens hebben we de vragen gecategoriseerd d.m.v. de moscow methode.
</p>
<p style="text-align:justify;">
Toen het tijd was om concepten te bedenken en deze ook op papier te zetten heb ik de creatieve techniek brainwriting 3-6-5 voor gesteld uit het boek: Design.Think.Make.Breake.Repeat. In combinatie met een COCD-box. Uit deze sessie kwamen drie concepten die wij goed konden onderbouwen door de informatie die wij bij de doelgroep en het NRC gekregen hadden. Ook haddem wij hier de design principles bij betrokken en de inside cards.
</p>
<p style="text-align:justify;">
De Define fase hebben wij afgesloten met drie concepten en een presentatie aan de opdrachtgever.
