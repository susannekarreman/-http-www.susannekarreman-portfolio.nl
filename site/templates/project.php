<p style="text-align:justify;">Ik ben samen met mijn team de discover fase begonnen met onderzoek. Ik wilde graag meer over de opdrachtgever weten om de opdracht beter te begrijpen. Ik heb deskresearch gedaan naar het NRC en hier een merkanalyse van gemaakt.
Al snel ben ik met een teamgenoot de straat op gegaan voor een merkwaarde test. Om te testen of de toekomstige nrc lezer ook al de waarde van het NRC kent.
Daarna ben ik verder gegaan met een observatie van mensen die de krant lezen en om te zien waar de kranten te vinden waren. Ik kwam erachter dat ik op het verkeerde moment van de dag was gaan observeren omdat het NRC nog niet geleverd was.
alle verzamelde data hebben we gestructureerd op de insight wal. Dit zorgt voor een duidelijk overzicht en het snel terugvinden van belangrijke data. Door gebruik te maken van de insight wall kan ik sneller data structureren en verbanden leggen. 
Om de aanmens die ontstaan waren te bevestigen of te ontkrachten heb ik een enquête opgesteld en heeft iedereen deze gedeeld via Instagram, Facebook en sociale kring. Hieruit kwamen 72 reacties. De conclusie uit de enquête was dat dat veel mensen niet willen betalen voor een betaald nieuws abonnement. Omdat ze niet weten waar ze voor betalen. De papieren krant zal zeker in de toekomst verdwijnen en wordt nu alleen nog maar gelezen door wat oudere abonnees. Op zaterdag wordt er meer tijd genomen om naar het nieuws te kijken of de zaterdagkrant te lezen. Het grootste deel vertrouwt onbekende nieuwsbronnen en leest dus vaker nepnieuws en gelooft dit dan ook. 
Met de informatie die ik had verzameld heb ik een user journey gemaakt met de pijnpunten de doelgroep. Dit heb gedaan om te kijken in welke oplosrichting ik zou kunnen kijken. 
De fase is afgesloten met een tussenpresentatie en een onderzoeksposter. Ik heb zelf deze presentatie gegeven, dit komt omdat ik het leuk vind om te doen.</p>
