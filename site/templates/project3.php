<h6><b>Presser cooker</b></h6>
<p style="text-align:justify;">
In week 5 is de ideation fase begonnen met een ideation presser cooker in de studio. Dit
werd gegeven door Bob. Doordat de ideation begon met de presser coocker hadden wij nog
geen concept idee. Aan het einde van deze dag zouden wij een meating hebben de studenten
va TI (Technische Informatica). Wij hebben deze dag gebruik gemaakt van vier technieken;
<li>Superhelden methode</li>
<li>Cocd box</li>
<li>HKW (hoe kunnen we)</li>
<li>Toevalstreffer </li> 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Na deze dag hebben wij vijf concepten bedacht waar wij nog niet helemaal achter stonden.
Het waren vijf snel bedachten ideeën. Door de presser cooker zijn wij als team wel heel erg
losgekomen. Wij hadden hiervoor erg veel moeite met het los laten van sommige ideeën en
doelen. Wij hebben uiteindelijk geen van deze ideeën gebruikt maar dat was ook nog niet
nodig in deze fase. 
</p>

<h6><b>Superhelden methode</b></h6>
<p style="text-align:justify;">
Om te beginnen tijdens de presser cooker hebben wij gekozen voor de techniek
superhelden. Ik had hier zelf nog nooit van gehoord. Ik vond het een heel leuke
techniek om te gebruiken. Door deze techniek kan je van alles bedenken en het
leuke is het hoeft niet echt te zijn of gemaakt te kunnen worden. Dit was een
goed begin omdat iedereen van alles kon verzinnen en niks was fout.
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Na deze oefening was iedereen klaar om echt te gaan beginnen. Door ze
oefening zijn we allemaal wat losser geworden. Ik zou deze oefening zeker nog
een keer gebruiken in de volgende periode. Ik heb geleer van deze oefening dat
als je creatief bezig bent dat er geen goede of foute antwoorden zijn. 
</p>

<h6><b>Toevalstreffer </b></h6>
<p style="text-align:justify;">
Deze techniek hebben wij ook binnen de presser cooker gebruikt. Wij moesten 1 woord
random opzoeken dat is het woord buurt geworden en daar hebben wij een woord web van
gemaakt. Hier kwamen heel veel verbindingen uit zoals bv met buurtpreventie de ouderen
helpen. De samenleving kwam ook naar voren omdat ouderen graag deel uit willen maken
van de samenleving maar ook de participatiesamenleving dat burgers voor hun ouderen
moeten zorgen. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Na de toevalstreffer hadden wij wel een nieuw inzicht gekregen op de situatie en hoe we
daarmee een concept konden gaan bedenken
</p>

<h6><b>HKW (hoe kunnen we?) </b></h6>
<p style="text-align:justify;">
Deze techniek was ook onderdeel van de presser cooker. We zijn begonnen om eerste
allemaal een eigen lijstje te maken met de hoofdvraag ‘hoe kunnen we erg voor zorgen dat
ouderen langer thuis kunnen blijven?’ van uit het oogpunt; familie, ouderen en de zorg. Door
elke keer de vraag te stellen hoe kunnen we? Zoals bv bij famillie hwk zorg sneller op
starten -> hwk algemeen advies geven -> hkw hoe kunnen we mantelzorgers langer fit
houden -> hwk familie motiveren om voor de ouderen te zorgen. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Door constant dezelfde vraag te blijven stellen kom je steeds vaker op een nieuw antwoord
zoals hwk ouderen ondersteunen door middel van een product? Op deze vraag zijn wij veder
gegaan met het convergeren van ideeën. 
</p>

<h6><b>COCD-box </b></h6>
<p style="text-align:justify;">
Wij hebben de COCD-box gebruikt om al onze ideeën te ordenen. Waardoor het
makkelijker was om te bepalen welke wel en welke niet haalbaar zijn en wat origineel is of
wat niet. Wij gebruikte de COCD-box vaak na een andere creatieve technieken zodat we
meer duidelijkheid en overzicht hadden na het divergeren van ideeën. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Ik vond de COCD-box een fijne techniek om te gebruiken omdat ik soms het overzicht
kwijtraak na het divergeren door de COCD-box kunnen wij makkelijk convergeren en
blijven er ideeën over waar we goed over na gedacht hebben. 
</p>

<h6><b>Reframing </b></h6>
<p style="text-align:justify;">
Door afwezigheid van 2 team genoten bleven alleen ik en Natasja over. Doordat we midden
in de ideation fase zitten moesten wij met z’n tweeën gaan brainstormen voor een concept.
Met zijn tweeën is brainstormen best lastig. Ik ben opzoek gegaan naar een creatieve
techniek in het boek ‘Desing. Think. Make. Break. Repeat. A handbook of methods’ Van BIS.
Hieruit hebben wij Reframing uitgevoerd om los te komen van de concept vraag waar we
niet veder mee kwamen.
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Ik heb van deze opdracht geleerd dat je de hoofdvraag zodanig kan manipuleren en
verdraaien naar je eigenhand, zolang je de veranderingen maar kan blijven onderbouwen
met argumenten en kan terugkoppelen op de hoofdvraag. 
</p>


<h6><b>Mooie Metrix  </b></h6>
<p style="text-align:justify;">
Een brainstorm techniek die je met zijn tweeën uit kan voeren is mooie metrix. Van deze
techniek hebben we een spel gemaakt om het actief te houden doordat we maar met zijn tweeën
waren. Door allebei een andere kleur postits te nemen konden we bijhouden wie wat op had
geschreven.
Van de metrix techniek hebben we vijf woorden gekozen en deze gebruikt om nieuwe ideeën
te bedenken die we dan later hebben categoriseren in de COCD-box 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Ik heb van deze opdracht geleerd dat je ook makkelijk met z’n tweeën kan brainstormen. Dat
je door deze techniek met je met de ideeën van elkaar een nieuw concept kan bedenken. Door
random gekozen woorden word je geforceerd om op een nieuwe manier na te denken om op
een nieuw concept te komen.
</p>

<h6><b>Brainwritting 6-3-5   </b></h6>
<p style="text-align:justify;">
Brainwritting is een techniek die ik heb voorgesteld uit mijn boek ‘Design. Think. Make.
Break. Repeat. A handbook of methods.’ Dit is een methode dat je op elkaars ideeën een nieuw
concept bedenkt. Door de oefening op papier te doen geef je ieder team lid een eerlijke kans
om zijn ideeën te delen. Deze oefening heeft ons als team erg geholpen om out of the box te
denken door met elkaars ideeën door te werken. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Ik heb van deze opdracht geleerd dat je goed kan door werken op het idee van andere
teamleden. Ik heb me door deze opdracht voorgenomen om meer te luisteren naar mijn team
genoten en mijn team genoten meer het word te laten nemen zodat zij ook meer hun mening
en ideeën kunnen delen. 
</p>

