    <h4 class="kwaliteiten"> Eindresultaat</h4>
<p style="text-align:justify;">
Ons concept heet Care for the future ook wel CFF genoemd. CFF is een platform voor
volwassenen en ouderen. Ons concept is gericht op de ouderen van de toekomst. Wij hebben
voor deze doelgroep gekozen omdat uit onderzoek is gebleken dat je ouderen tot 70 jaar
technisch nog wat kan leren, dit wordt moeilijker naarmate ze ouder worden. Het grootste
probleem moet nog komen, de vergrijzing komt eraan. Dat betekent nog meer ouderen.
Het platform CFF is bedoeld om de communicatie van belangrijke zorg informatie over de
patiënt te kunnen communiceren in een medisch dossier tussen verschillende belangrijke
partijen zoals de huisarts, zorgverzekeraar en zorginstelling. Dit zijn partijen die nu ook al
toegang hebben tot deze informatie maar allemaal via een ander platform.
CFF zorgt voor controle veiligheid en overzicht doordat elke keer wanneer er iets in jouw
document wordt aangepast dat elke partij die toegang heeft, toestemming moet geven voor
deze wijziging. Dit willen via blockchain doen. Ons concept is gebaseerd op het principe van
blockchain omdat dat een van de veiligste manieren is om te doen. Zodat er maar 1 kopie
van het document is en dat iedere partij dezelfde kopie heeft. Er kan dus niks aangepast
worden zonder de toestemming van de patiënt (ouderen/ hoofgebruiker).
Ook is een zorgplan opstellen een onderdeel van CFF. De patiënt krijgt een melding
wanneer hij/zij 60 is geworden om een zorgplan op te stellen. In dit zorgplan stel hij/zij op
wat er moet gebeuren als er iets verandert binnen de gezinssamenstelling of dat er iets in
zij/haar situatie veranderd. Zodat wanneer er iets met hem/haar gebeurt hij/zij zich geen
zorgen meer hoeft te maken over wat er nou moet gebeuren. Door dat hij/zij iets in het
dossier verandert kan er je zorgplan in werking gezet worden. 
</p>

