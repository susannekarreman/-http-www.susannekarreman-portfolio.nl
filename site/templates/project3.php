<h4 class="kwaliteiten"> Preaparation</h4>
<p style="text-align:justify;">
In deze fase heb ik mijn team leren kennen, maar heb ik ook al de eerste
voorbereidingen genomen om te beginnen met mijn onderzoek. Hierbij heb ik geleerd
wat de opdrachtgever van ons verwachte maar ook hoe de samenwerking binnen ons
toen was. Hierbij hebben wij als team gekeken hoe we het beste konden beginnen en
welke methodes en techniek daarbij pasten. </p>

<h6><b>User journey</b></h6>
<p style="text-align:justify;">
Als onderdeel van het plan van aanpak heb ik samen met Timon een user journey eerst
getekend en daarna uitgeschreven. In de user journey hebben wij het verhaal van Rogier van
online departement als uitgaanspunt genomen. Wat ging er mis en zijn situatie en wat zou
hier de gewenste situatie geweest zijn? Door telkens deze vraag te blijven stellen kwamen
wij op een beeld van de probleemstelling.
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Voor mij was het heel nuttig een user jourey te maken omdat ik dit nog nooit eerder had
gedaan, en er veel van geleerd heb. Ik heb geleerd hoe je, je zelf moet verplaatsen in iemand
anders zijn schoenen. Door dat te doen kreeg ik beter gevoel bij deze opdracht. Waarna ik
geïnterneerd en vol positieve energie begonnen ben aan mijn onderzoek. 
</p>

<h6><b>Scrum</b></h6>
<p style="text-align:justify;">
Ik heb eerst via een workshop over scrum van mijn docent Bob Joziasse geleerd wat scrum is en hoe je het toe
kan passen binnen je team. Wij als team hebben een scrum boord gemaakt om zo onze
planning te visualiseren en een duidelijk overzicht te hebben waar van iedereen mee bezig is.
Wij hebben het scrum boord gemaakt van een groot A1 vel met daar op de onderdelen; to
do, doing en done. Zo kwamen de plaknoties onder elke kopje te staan we wie die taak op
zich nam en ervoor zorgde dat die naar het kopje ‘done’ kon gaan. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Door de planning op deze manier weer te geven hebben tijdens het hele proces een duidelijk
overzicht gehad met wie wat aan het doen was, maar nog belangrijker een overzicht van wat
ik zelf moest doen. Ik ga zeker in de volgende periode scrum weer gebruiken omdat het voor
mijzelf erg fijn werkt.  
</p>

