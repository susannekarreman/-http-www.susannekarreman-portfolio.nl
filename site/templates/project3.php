    <p style="text-align:justify;"> Voor de opdrachtgever Online department heb ik de volgende vraag onderzocht:
Hoe zorgen wij ervoor dat ouderen langer thuis kunnen blijven wonen met behulp van
mantelzorgers? </p>

<h4> De aanleiding van opdracht</h4>
<p style="text-align:justify;">
De vraagstelling is verteld door Rogier Kien. Rogier is UX-lead bij Online department. Hij
ontwerpt Betekenisvolle en aangename gebruikservaringen, op het gebied van websites,
softwareprogramma’s en apps.
Naar aanleiding van Rogiers persoonlijke ervaring hebben wij de opdracht ontvangen.
Rogiers moeder lijdt aan de ziekte van Alzheimer, zijn vader trad op als mantelzorger. Dat
ging goed, totdat Rogiers vader zelf ziek werd. Toen vielen de taken op Rogier en zijn broer
en zus. Toen zij bezig waren met het opstarten van de zorg van hun moeder liepen zij tegen
velen problemen aan zoals;
<li>Er is geen centraal meldpunt, je bent constant in gesprek met verschillende
zorginstanties</li>
<li> Waardevolle informatie raakt kwijt</li>
<li> Ze werden van het kasje naar de muur gestuurd</li>
<li> Rogier moest naast zijn fulltimebaan en gezin voor zijn moeder zorgen.</li> </p>

