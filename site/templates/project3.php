<h6><b>Deskresearch</b></h6>
<p style="text-align:justify;">
AIk ben mijn onderzoek begonnen met deskresearch op google scholar. Ik heb hiervoor
gekozen omdat je hier veel verschillende soorten wetenschappelijke onderzoeken tegen
komt. Met zo veel verschillende bronnen en artikelen was het soms moeilijk om de
betrouwbaarheid en beruikbaarheid te bepalen van verschillende artikelen en bronnen. Om
de bruikbaarheid en betrouwbaarheid te kunnen testen heb ik de CRAAP-methode
toegepast. Die ik geleerd heb ik de workshop ‘van opdracht naar onderzoek’.
    </p>
<p style="text-align:justify;">
Om inzicht te krijgen in de problemen van de vraagstellening ben ik mijn deskresearch
begonnen bij de hoofdpersonen de ouderen.
Wat voor de zorg de ouderen nodig hebben maar ook het verschil tussen ouderen, ouderen
met een ziekte en ouderen zonder ziekt met alleen ouderdomskwaaltjes. Door steeds dieper
het internet in te duiken ontstond er bij mij een sneeuwbaleffect. Hierdoor ben ik tijdens
mijn onderzoek veel verschillende soorten onderzoeken tegen gekomen zoals;

<li style="text-align:justify;">Een documenteren Zorg aan huis (De Monitor, 20171) hier heb ik een beeld gekregen
over hoe hoog de werkdruk van verzorgende is, maar ook hoe het voor ouderen is om
afhankelijk te zijn van iemand anders. Ik heb dit meegenomen in de rest van mijn
onderzoek en mijn ontwerp. </li>
<li style="text-align:justify;"> Ik heb onderzoek naar bestaande zorgmodellen gekeken waar ouderen langer thuis
kunnen blijven wonen. Zoals Samenoud uit Noorden van Nederland (Samenoud,
20152) hiervan heb ik de ervaring van ouderen onderzocht en hoe deze organisatie te
werk is gegaan. Het resultaat daarvan is uiteindelijk op de insghtwall terecht
gekomen maar ik heb het niet bewust meegenomen in de rest van mijn ontwerp. </li>
<li style="text-align:justify;">Ik heb onderzoek gedaan naar preventieve activiteiten voor ouderen (RIVM, 20183)
Hieruit is gebleken dat er veel ouderen zijn die in de maatschappij niet goed bereikt
worden. Ook bleek hieruit dat er verschillende soorten preventieve activiteiten
bestonden voor ouderen en dat dit heel veel geld kost, maar ook dat er voldoende
vrijwilligers moeten zijn die er niet zijn. Het resultaat hiervan is dat ik hier lang 
6
onderzoek naar heb gedaan en uiteindelijk heb besloten dit dat soort ouderen niet
mijn doelgroep zullen zijn. </li>
<li style="text-align:justify;">Ik heb onderzoek gedaan naar bewegen onder ouderen en hoe dit ouderen kon
helpen om langer thuis te blijven wonen. Ik heb hier van het onderzoek gebruikt van
(Wijlhuizen, G.J. · Tak, E.C.P.M. · Chorus, A.M.J., 20114) over Preventief bewegen
in instellingen in de ouderenzorg. </li>
<li style="text-align:justify;">Ook heb ik de bewegingsrichtlijnen van de gezondheidsraad geraadpleegd.
(Gezondheidsraad 20175) Dit heeft mij aan het denken gezet hoe wij ouderen kunnen
helpen door ze meer te laten bewegen. </li>
<li style="text-align:justify;">Ik heb onderzoek gedaan naar Volksgezondheid toekomst en verkenning (VTV)
(Rijksinstituut voor Volksgezondheid en Milieu, 20186) om een overzicht te krijgen
wat de overheid nu op het moment voor de ouderen doet. </li>
<li style="text-align:justify;">Ik heb onderzoek gedaan naar de kwetsbaarheid van ouderen. (loketgezondleven.nl,
20187
) Hier uit is gebleken dat ouderen eenzaam zijn, kwetsbaar zijn en dat ze een
lage veerkracht hebben. </li>
<li style="text-align:justify;">Ik heb onderzoek gedaan naar valpreventie en het vallen onder ouderen.
(loketgezondeten.nl, 20178) Hier uit is gebleken dat er veel doden zijn bij 65 plussers
die vallen. Ook zijn er veel ouderen die na hun val meer zorg nodig hebben. </li>
<li style="text-align:justify;">Ik heb onderzoek gedaan naar de mantelzorger en hoe we die minder kunnen
overbelasten (movisie, 20109) Hieruit is geleken dat nauw samen werken met
organisaties een must is. </li>
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Ik heb mijn deskresearch uitgeprint en op de insightwall gehangen om een duidelijk
overzicht te creëren. Na mijn deskresearch ben ik erachter gekomen dat het probleem nog
veel groter is dan dat ik al dacht. Ouderen vragen veel zorg die er op het moment niet is. Je
moet de zorg beter gaan verspreiden en meer mensen in zetten, want het grootste probleem
moet nog komen.  
</p>

<h6><b>Doelgroep onderzoek 1</b></h6>
<p style="text-align:justify;">
In een verzorgingstehuis in Schiedam ben ik langs geweest bij meneer en mevrouw
Nederpijl. Meneer en mevrouw wonen daar omdat mevrouw dementerend is en meneer niet
meer voor mevrouw kon zorgen. Dit werd meneer namelijk vooral mentaal te zwaar. Hij
piekerde ’s nachts veel over hoe het nou in de toekomst veder moet. Wat nou al er wat met
hem zou gebeuren wie zorgt er dan voor mevrouw? Hierdoor ging hij fysiek ook achteruit
omdat hij erg weinig slaapte en de zorg voor mevrouw gewoon door ging. Zelf toen meneer
twee keer per dag hulp krijg met de zorg van mevrouw was het voor hem te zwaar hij was
uitgeput. Nu ze in het tehuis wonen heeft meneer weer rust gekregen en kan hij weer
genieten van het leven. Voor mevrouw was de verhuizing erg zwaar en dit heeft haar
gezondheid nog veder beinvloed. Dit komt omdat ze uit haar dagelijkse routine is gehaald en
in een andere omgeving is neer gezet. Het heeft ongeveer zes maanden geduurd voor
mevrouw gewend was. Nu wonen ze samen gelukkig in het verzorgingstehuis en krijgt
mevrouw en meneer de zorg die ze beiden nodig hebben.  
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Voor dit koppel is het verstandig geweest om naar een verzorgingstehuis te gaan. Omdat ze
zorg voor mevrouw eenmaal heel complex is en dat vraagt om 24/7 zorg. Meneer heeft nu
zijn rust weer terug en kan nu weer genieten van zijn vrouw i.p.v. haar te zien al een last. Ze
krijgen nu allebei de zorg die ze nodig hebben en dat is goed voor hun gezondheid. Als
mevrouw niet de ziekte van Alzheimer zou hebben, zouden ze nog een lange tijd thuis
kunnen blijven wonen. Daardoor heb ik besloten dat ik ouderen met de ziekte van Alzheimer
niet als doelgroep wilde hebben.
</p>

<h6><b>Doelgroep onderzoek 2</b></h6>
<p style="text-align:justify;">
In het verzorgingstehuis ben ik langs geweest bij mevrouw de Bruin. Mevrouw de Bruin is
veertig jaar alleen en heeft altijd voor zichzelf gezorgd tot dat ineens niet meer ging.
Mevrouw kwam het tehuis binnen toen ze niet meer voor zichzelf kon zorgen, dit kwam
door dat ze niet meer goed kon lopen. Ze kon niet meer zelf douche, zichzelf aankleden, voor
zichzelf koken etc. Doordat ze haar klachten niet goed kon benoemen wist de huisarts niet
wat mevrouw had en kon de juiste medicijnen niet worden voorgeschreven. Door dat ze in
het te huis 24/7 geobserveerd werd, werd duidelijk dat mevrouw de ziekte van Parkinson
heeft. Hierdoor werd de juiste medicijnen en fysiotherapie voor geschreven. Mevrouw kan
nu weer zelfstandig lopen en opstaan. Ze mist wel heel erg haar huis, ze houdt graag van
buiten en zit nu in een kamer met twee ramen die niet eens open kunnen. Thuis had ze een
groot balkon waar ze graag op zat. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Mevrouw de Bruin had langer thuis kunnen blijven wonen als de juiste diagnose was geteld
en de juiste medicijnen was voor geschreven. In het tehuis krijgt ze de zorg en de aandacht
die mevrouw nodig heeft.
</p>

