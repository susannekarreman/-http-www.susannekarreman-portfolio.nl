<h6><b>Design principles</b></h6>

<p style="text-align:justify;">
Om straks tijdens de ideation een werkbaar concept neer te zetten moeten we
eerst de design priniples opstellen. Design principles zijn onderlenen waar je
concept/product aan moet voldoen. Tijdens het opstellen hier van zijn wij
human-centert te werk gaan. Wij hebben van uit de gebruiker na gedacht.
Wij hebben er zelf vijf opgesteld;</p>
<li style="text-align:justify;">De zorg ontzorgen</li>
<p style="text-align:justify;"><i>We willen dat ouderen zelfstandig taken op zich nemen zodat de (mantel)zorg zich kan richten op de belangrijke taken. Zij nemen dan de taken op zich, waarvoor zij zij echt nodig zijn.</i></p>

<li style="text-align:justify;">Ouderen staan centraal</li>
<p style="text-align:justify;"><i>Omdat we ons richten op de zelfredzaamheid van ouderen, denken we vanuit het perspectief van de ouderen.</i></p>

<li style="text-align:justify;">Makkelijk in gebruik</li>
<p style="text-align:justify;"><i>Het product is na een korte uitleg bruikbaar voor ouderen. Als het gebruik logisch is, wordt het voor de ouderen makkelijker om te gebruiken.</i> </p>

<li style="text-align:justify;"> Zelfredzaamheid bevorderen</li>
<p style="text-align:justify;"><i>Doordat de ouderen zelfstandiger worden kunnen zij de dagelijkse taken goed
uitvoeren, waardoor de zelfredzaamheid vooruit gaat.</i></p>

<li style="text-align:justify;"> Betaalbaar voor de gemiddelde ouderen </li>
<p style="text-align:justify;"><i>Niet elke oudere heeft een breed pensioen om dure zorg in te kopen, maar we willen ons product kunnen aanbieden aan iedereen. </i></p>

<h6><b>Onderzoeksresultaten </b></h6>
<p style="text-align:justify;">
De onderzoeksresultaten komen van de insightwall. Ons resultaat is de visuele cirkel die ik
ook visueel gemaakt heb en ook op de muur heb gehangen. Wij zijn om deze cirkel gekomen
omdat op de muur te zien is dat elke catogorie te maken heeft met een andere. Zoals
depressie kan leiden tot minder eetlust wat weer kan leiden tot ondervoeding, door
ondervoeding neemt de spierkracht af waardoor de kans op vallen groter wordt en wanneer
een oudere valt kan dit leiden tot meer zorg maar ook kan dit leiden tot angst, wat weer kan
leiden naar depressie.
Door deze resultaten willen wij deze cirkel verbreken en er zo voor zorgen dat ouderen
langer thuis kunnen blijven wonen. Ook kwam uit onderzoek dat ouderen toch liever thuis
willen blijven wonen en als het echt niet anders kan dat ouderen dit zelf moeten accepteren.
Ouderen vinden het ook moeilijk als een onbekende ineens voor hun moet zorgen, ze missen
de vertrouwensband met hun verpleger. Dit komt omdat het vaak onpersoonlijk is omdat de
verpleger maar weinig tijd heeft. Ouderen willen graag langer en zo veel mogelijk mee doen
aan de samenleving ze willen zich niet nutteloos voelen, dat maakt het vaak voor ouderen
ook erg moeilijk om hulp te vragen. Ouden schamen zich en willen lieven niemand tot last
zijn. 
</p>
<p style="text-align:justify;">
<b>Conclusie</b> <br>
Wij als team waren erg te vreden en trots over onze onderzoeksresulaten omdat wij iets
gevonden hadden wat andere teams niet hadden, de visuele cirkel. De onderzoeksresultaten
heeft ons veder geholpen in ons proces zoals met het op zetten van onze design principles. 
</p>

