<h4 class="kwaliteiten">Ontwerpvraag</h4>
    <p style="text-align:justify;"> Hoe kun je studenten motiveren en stimuleren tot duurzame en gezonde voedingskeuzes? </p>
   
    <h4 class="kwaliteiten">De opdracht</h4>
    <p style="text-align:justify;"> Ontwerp een interactieve interventie waarmee studenten in Rotterdam gestimuleerd worden tot
duurzame en gezonde voedingskeuzes. De interventie ontstaat vanuit de (data) inzichten die je met
elkaar hebt verkregen. Dit betekent dat je nauw met de eindgebruiker samenwerkt en gebruik maakt
van verschillende methodes zoals observatie, monitoring en andere samenwerkingsmethodes.
Tezamen proberen jullie tot inzichten te komen om het gedrag m.b.t. gezonde voeding van jongeren
te bevorderen. Dit hoeft niet te betekenen dat de opdracht zich richt op het individuele gedrag van
studenten in Rotterdam. Het zou ook een interactieve interventie in de openbare ruimte kunnen zijn
die duurzame en gezonde voeding stimuleert.</p>

