    <h4>De opdracht</h4>
    <p style="text-align:justify;"> Voor de opdrachtgever Online department heb ik de volgende vraag onderzocht:
Hoe zorgen wij ervoor dat ouderen langer thuis kunnen blijven wonen met behulp van
mantelzorgers? </p>

<h4> De aanleiding van opdracht</h4>
<p style="text-align:justify;">
De aanleiding van de opdracht is het veder uitwerken van een gekozen concept van DC2, dat
ervoor moet zorgen dat ouderen langer thuis kunnen blijven wonen. Ik heb samen met mijn
team gekozen voor de briefing van fier om ouderen met dementie te helpen een dagelijks taak
zelfstandig en eenvoudig uit te voeren. Het team van het concept Fier hebben het concept
ontwikkeld om ouderen met dementie aan te sporen om een dagelijkse taak zelfstandig uit te
voeren met behulp van sensoren en technologie. </p>
<p style="text-align:justify;">
Uit interviews die ik heb gehouden in het vorige kwartaal kwam terug dat ouderen moeite hebben met boodschappen doen.
 Ook uit deskresearch bleek dat mantelzorgers vaak de taak van
boodschappen op zich nemen wat tijd in beslag neemt. (Mantelluisteren, 2014) "Ouderen
voelen zich vaak bezwaard om zo veel hulp te vragen dit verlaagt hun eigen waarden". 
</p>

<h4> Huidige sitautie</h4>
<p style="text-align:justify;">

<li>Tijdens het boodschappen doen vergeten wat je moet halen</li>
<li>Producten dubbel kopen, omdat je denkt ze niet meer te hebben</li>
<li>Lichamelijke klachten vanwege het tillen van zware boodschappentassen</li>
<li>Eten weggooien omdat je niet bewust ben van wat je voorraad is</li>
<li>Minder gevarieerd eten
</p>
<p style="text-align:justify;">
De huidige situatie laat zien dat ouderen vaak niet zelfstandig boodschappen kunnen doen,
hiervoor is vaak de hulp nodig van mantelzorgers, familie leden of zelf vrijwilligers nodig. Dit
willen wij gaan aanpakken zodat ouderen zelfstandig boodschappen kunnen doen en hun
eigen waarden kunnen behouden. 
</p>

<h4> Gewenste sitautie</h4>
<p style="text-align:justify;">
<li>Nooit meer vergeten wat je moet halen</li>
<li>Fysieke rust, door thuisbezorging dienst</li>
<li>Gezond en gevarieerd eten</li>
<li>Zelfstandig, zonder hulp van mantelzorger boodschappen doen</li>
</p>

<h4> Eindresultaat</h4>
<p style="text-align:justify;">
We weten allemaal hoe vervelend het is om wat te vergeten tijdens het boodschappen doen,
laat staan voor ouderen met bijvoorbeeld een lichte vorm van dementie!
FoodCam biedt hiervoor de oplossing door middel van een camera die je in de koelkast en
voorraadkasten kan plaatsen, waarbij de camera door middel van food recognition bijhoud
wat je momenteel in huis hebt en wat er over datum is.
Deze informatie wordt doorgespeeld naar de bijbehorende app, waar je boodschappenlijstje
automatisch wordt aangepast hierop! Deze app is overal beschikbaar, waardoor je nooit te
weinig of te veel ingrediënten in huis hebt.
Als je even niet weet wat je wilt eten deze week kan je bij Recepten inspiratie vinden, waarbij
FoodCam de ontbrekende ingrediënten filtert en toevoegt aan je boodschappenlijst.
Als boodschappen doen fysiek te zwaar is voor je hoef je je ook geen zorgen te maken, want
via de app kan je makkelijk de producten op je boodschappenlijstje thuis laten bezorgen!
/Oftewel zorgt FoodCam ervoor dat ouderen zonder hulp hun boodschappen kunnen
regelen, waardoor hun eigenwaarde wordt bevorderd. De mantelzorger is hiervoor niet meer
nodig, waardoor hun de tijd krijgen om de goede zorg aan de ouder te verlenen.
</p>


